// Un script JavaScript qui permet de convertir une chaîne de caractères en nombre (instruction Number)
// La documentation complète en français de Number: https://developer.mozilla.org/fr/docs/Web/JavaScript/Reference/Objets_globaux/Number

var a = "5";
console.log(typeof a); // Affiche 'string' car la variable contient du texte

a = Number(a); // Conversion du texte en un nombre
console.log(typeof a); // Affiche bien 'number' 

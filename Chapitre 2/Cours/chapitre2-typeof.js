// Un script JavaScript qui permet de connaître le type d'une variable (instruction typeof)
// La documentation complète en français de typeof : https://developer.mozilla.org/fr/docs/Web/JavaScript/Reference/Op%C3%A9rateurs/L_op%C3%A9rateur_typeof

var a = 5;// Une variable qui contient un nombre
var texte = "Bonjour !"; // Une variable qui contient une chaîne de caractères

console.log(typeof a); // Affiche 'number'
console.log(typeof texte); // Affiche 'string'
